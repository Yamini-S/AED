    private void populateAuthorityTable(){
        DefaultTableModel model = (DefaultTableModel) authorityTable.getModel();
        model.setRowCount(0);
        
        HOAuthority authority = ecoSystem.getAuthority();
        for(UserAccount userAccount:authority.getUserAccountDirectory().getUserAccountList()){
            Object[] row = new Object[6];
            row[0] = authority.getAuthorityCode();
            row[1] = authority;
            row[2] = authority.getEmailId();
            row[3] = authority.getAddress().getCity();
            row[4] = userAccount.getUserName();
            row[5] = userAccount.getPassword();
            model.addRow(row);
        }
    }

