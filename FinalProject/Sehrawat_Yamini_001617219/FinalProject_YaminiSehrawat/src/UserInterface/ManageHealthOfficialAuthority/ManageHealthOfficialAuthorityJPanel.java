    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
       int selectedRow = authorityTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a authority to update");
            return;
        } else {
            HOAuthority authority = (HOAuthority) authorityTable.getValueAt(selectedRow, 1);
            UpdateHealthOfficialAuthorityJPanel panel = new UpdateHealthOfficialAuthorityJPanel(userProcessContainer, authority);
            userProcessContainer.add("UpdateHealthOfficialAuthorityJPanel", panel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_updateBtnActionPerformed

