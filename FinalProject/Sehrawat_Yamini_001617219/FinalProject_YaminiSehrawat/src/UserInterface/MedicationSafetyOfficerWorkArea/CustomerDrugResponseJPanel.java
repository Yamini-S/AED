    private void callBackBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_callBackBtnActionPerformed
        int selectedRow = issuesTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row first!");
            return;
        }
        DrugIssueRequest dRequest = (DrugIssueRequest)issuesTable.getValueAt(selectedRow, 4);
        Drug drug = (Drug) issuesTable.getValueAt(selectedRow,1);
        if((dRequest.getCallBackRequest().equals("Yes"))){
            JOptionPane.showMessageDialog(null, "Drug already removed from the market!");
            return;
       }else{
            dRequest.setCallBackRequest("Yes");
            dRequest.setStatus("Drug Withdrawn");
            
            for(Drug drug1:ecoSystem.getDrugCatalog().getDrugCatalog()){
                if(drug1.getDrugId() == drug.getDrugId()){
                    drug1.setStatus("Drug Disapproved");
                }
            }
            for (Drug drug2 : enterprise.getDrugCatalog().getDrugCatalog()) {
                if (drug2.getDrugId() == drug.getDrugId()) {
                    drug2.setStatus("Drug Disapproved");
                }
            }
        }
        populateDrugComboBox();
        Drug d = (Drug) drugComboBox.getSelectedItem();
        populateDrugIssueTable(d);
    }//GEN-LAST:event_callBackBtnActionPerformed

    private void drugOkBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_drugOkBtnActionPerformed
        int selectedRow = issuesTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row first!");
            return;
        }
        
        DrugIssueRequest dRequest = (DrugIssueRequest)issuesTable.getValueAt(selectedRow, 4);
        Drug drug = (Drug) issuesTable.getValueAt(selectedRow,1);
        
        if(dRequest.getStatus().equals("Drug Success")){
           JOptionPane.showMessageDialog(null, "Respond alredy sent as SUCCESS!");
           return;
       }
        
        if(dRequest.getCallBackRequest().equals("Yes")){
           JOptionPane.showMessageDialog(null, "Drug has already been called back");
           return; 
       }
        
        dRequest.setStatus("Drug Success");
        dRequest.setCallBackRequest("No");
        populateDrugIssueTable(drug);
    }//GEN-LAST:event_drugOkBtnActionPerformed

