    private void populateDrugComboBox(){
        drugComboBox.removeAllItems();
        for(Drug drug:ecoSystem.getDrugCatalog().getDrugCatalog()){
            if(drug.getStatus() == null){
                return;
            }
            if(drug.getStatus().equals("Approved")){
                if (drug.getPharmaceuticalId() == enterprise.getEnterpriseId()) {
                    drugComboBox.addItem(drug);
                }
            }
        }
    }
    
    private void populateDrugIssueTable(Drug drug){
        DefaultTableModel model = (DefaultTableModel) issuesTable.getModel();
        model.setRowCount(0);
        
        for(WorkRequest workRequest:drug.getWorkQueue().getWorkRequestList()){
            if(workRequest instanceof DrugIssueRequest){
             drugIssueRequest = (DrugIssueRequest)workRequest;
             Object[] row = new Object[7];
             row[0] = drugIssueRequest.getRequestID();
             row[1] = drug;
             row[2] = drugIssueRequest.getDrugSafetyIssue();
             row[3] = drugIssueRequest.getSender() == null? ("N/A") : drugIssueRequest.getSender().getPerson().getName();
            // row[4] = drugIssueRequest.getOrganizationName();
             row[4] = drugIssueRequest;
             row[5] = drugIssueRequest.getCallBackRequest();
             row[6] = drugIssueRequest.getRdate();
             model.addRow(row);
            }
        }
    }
