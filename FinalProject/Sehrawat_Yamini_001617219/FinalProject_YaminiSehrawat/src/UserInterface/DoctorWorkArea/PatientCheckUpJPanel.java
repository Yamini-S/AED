    private void checkMedicalRecordBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkMedicalRecordBtnActionPerformed
        int selectedRow = patientInfoTable.getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null, "Please select a row to check Patient medical record!");
            return;
        }
        
        Patient patient = (Patient) patientInfoTable.getValueAt(selectedRow, 1);
        if (patient != null) {
            PatientMedicalRecordJPanel panel = new PatientMedicalRecordJPanel(userProcessContainer, patient, organization, userAccount, enterprise, ecoSystem);
            userProcessContainer.add("PatientMedicalRecordJPanel", panel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_checkMedicalRecordBtnActionPerformed

    private void searchPatientBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchPatientBtnActionPerformed
       int searchPatientId = 0;
       try{
           searchPatientId = Integer.parseInt(searchTxt.getText());
       }
       catch(Exception e){
           JOptionPane.showMessageDialog(null, "Please enter a numeric value for patient Id!");
            return;
       }
       if(searchPatientId == 0){
           JOptionPane.showMessageDialog(null, "Please enter patientID to search!");
           return;
       }
       if(searchPatientId<0){
           JOptionPane.showMessageDialog(null, "Please enter a valid patientID to search!");
           return;
       }
       Patient patient = enterprise.getPersonDirectory().getPatientDirectory().searchPatient(searchPatientId);
       if(patient == null ) {
            JOptionPane.showMessageDialog(null, "No patient found in this hospital!");
            return; 
        }
        if (patient != null) {
            populatePatientInfo(patient);
        }
    }//GEN-LAST:event_searchPatientBtnActionPerformed

