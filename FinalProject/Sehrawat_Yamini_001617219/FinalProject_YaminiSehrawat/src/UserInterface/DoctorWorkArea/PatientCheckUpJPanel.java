    private void populatePatientInfo(Patient patient){
        DefaultTableModel model = (DefaultTableModel) patientInfoTable.getModel();
        model.setRowCount(0);
        
        Object row[] = new Object[4];
        row[0] = patient.getPatientId();
        row[1] = patient;
        row[2] = patient.getAge();
        row[3] = patient.getGender();
        model.addRow(row);
    }

