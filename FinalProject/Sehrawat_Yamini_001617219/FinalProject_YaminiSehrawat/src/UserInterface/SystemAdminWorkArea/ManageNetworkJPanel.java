    private void submitBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitBtnActionPerformed
        String cityName = networkNameTxt.getText();
        if((cityName.equals(null)) || (cityName.equals(""))){
            JOptionPane.showMessageDialog(null, "Please enter valid Network name!");
            return;
        }
        Boolean value = ecoSystem.searchIfPresent(cityName);
        if(value == true){
            JOptionPane.showMessageDialog(null, "Network name already present. Please add another network");
            return;
        }
        ecoSystem.createAndAddNetwork(cityName);
        populateNetworkTable();
        networkNameTxt.setName(null);
    }//GEN-LAST:event_submitBtnActionPerformed

    private void backBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backBtnActionPerformed
        userProcessContainer.remove(this);
        CardLayout cardLayout = (CardLayout) userProcessContainer.getLayout();
        cardLayout.previous(userProcessContainer);
    }//GEN-LAST:event_backBtnActionPerformed


