    private void deleteEnterpriseBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteEnterpriseBtnActionPerformed
        
        int selectedRow = enterpriseTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row to delete!");
            return;
        }
        int enterpriseId  = (Integer)enterpriseTable.getValueAt(selectedRow,0);
        
        Network network = (Network)networkComboBox.getSelectedItem();
        //Enterprise enterprise = enterpriseTable.get
        
        int flag= JOptionPane.showConfirmDialog(this, "Are you sure want to remove?","Warning", JOptionPane.YES_NO_OPTION);
        
        if(flag==0){
            Enterprise enterprise = network.getEnterpriseDirectory().searchEnterprise(enterpriseId);
            if(enterprise!=null){
            network.getEnterpriseDirectory().getEnterpriseList().remove(enterprise);
          }
        }
        network = (Network) networkComboBox.getSelectedItem();
        populateEnterpriseTable(network);
    }//GEN-LAST:event_deleteEnterpriseBtnActionPerformed

