       private void populateNetworkComboBox() {
        networkComboBox.removeAllItems();

        for (Network network : ecoSystem.getNetworkList()) {
            networkComboBox.addItem(network);
            populateEnterpriseTable(network);
        }
    }
    
    private void populateEnterpriseTable(Network network){
        DefaultTableModel model = (DefaultTableModel) enterpriseTable.getModel();
        model.setRowCount(0);
        
            if((network!=null)&&(network.getEnterpriseDirectory() != null)){
            for(Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()){
                Object row[] = new Object[4];
                row[0] = enterprise.getEnterpriseId();
                row[1] = enterprise.getEnterpriseType().getValue();
                row[2] = enterprise;
                row[3] = enterprise.getAddress().getCity();
                model.addRow(row);
            }
            }
    }

