    private void createBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createBtnActionPerformed
        Network network = (Network) networkComboBox.getSelectedItem();
        Enterprise enterprise = (Enterprise) enterpriseComboBox.getSelectedItem();
        
        String uName = userNameTxt.getText().trim();
        String password = String.valueOf(passwordField.getPassword());
        String adminName = adminUserNameTxt.getText().trim();
        String street = streetTxt.getText().trim();
        String city = cityNameTxt.getText().trim();
        String state = stateNameTxt.getText().trim();
        String zipCode = zipCodeTxt.getText().trim();
        
        if (network == null || enterprise == null) {
            JOptionPane.showMessageDialog(null, "Invalid Input!");
            return;
        }
        if (uName.equals("") ||uName.equals(null)|| password.equals("")||password.equals(null)
                || adminName.equals("")||adminName.equals(null) || street.equals("") || street.equals(null)  ||  city.equals("")|| city.equals(null)
                || state.equals("") || state.equals(null)||zipCode.equals("")||zipCode.equals(null)) {
            JOptionPane.showMessageDialog(null, "Some of the value missing, Please fill all the details!");
            return;
        }
        
        String userNameCheck = ecoSystem.getUserAccountCheckDirectory().validUserAccountCheck(uName);
        if(userNameCheck == null){
            JOptionPane.showMessageDialog(null, "Username already present, please enter another username!");
            return;
        }
        
        Employee employee = enterprise.getPersonDirectory().getEmployeeDirectory().createEmployee(adminName, street,
                city, state, zipCode);
        if (enterprise.getEnterpriseType().getValue().equals("Hospital")) {
            UserAccount userAccount = enterprise.getUserAccountDirectory().createUserAccount(uName,
                    password, employee, new AdminRole());
        } else if (enterprise.getEnterpriseType().getValue().equals("Pharmacy")) {
            UserAccount userAccount = enterprise.getUserAccountDirectory().createUserAccount(uName,
                    password, employee, new PharmacyAdminRole());
        } else {
            UserAccount userAccount = enterprise.getUserAccountDirectory().createUserAccount(uName,
                    password, employee, new PharmaceuticalAdminRole());
        }
        populateEnterpriseAdminTable(network);
        ecoSystem.getUserAccountCheckDirectory().addUserAccountName(uName);
        JOptionPane.showMessageDialog(null, "Enterprise Admin account created successful1y!");
        resetFields();
    }//GEN-LAST:event_createBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        int selectedRow = enterpriseAdminTable.getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null, "Please first select a row to delete.");
        }
        
        UserAccount userAccount = (UserAccount) enterpriseAdminTable.getValueAt(selectedRow,1);
        int flag= JOptionPane.showConfirmDialog(this, "Are you sure want to remove?","Warning", JOptionPane.YES_NO_OPTION);
        
        if(flag==0){
            for (Network network : ecoSystem.getNetworkList()) {
                for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                     enterprise.getUserAccountDirectory().getUserAccountList().remove(userAccount);
                     
                }
            }
        }
        Network net = (Network) networkComboBox.getSelectedItem();
        populateEnterpriseAdminTable(net);
        JOptionPane.showMessageDialog(null, "Record deleted successfully!");
    }//GEN-LAST:event_deleteBtnActionPerformed

