    private void populateNetworkComboBox(){
        networkComboBox.removeAllItems();

        for (Network network : ecoSystem.getNetworkList()) {
            networkComboBox.addItem(network);
            populateEnterpriseAdminTable(network);
        }
    }
    
    private void populateEnterpriseComboBox(){
         enterpriseComboBox.removeAllItems();
         Network network = (Network) networkComboBox.getSelectedItem();
         for(Enterprise enterprise:network.getEnterpriseDirectory().getEnterpriseList()){
             enterpriseComboBox.addItem(enterprise);
             populateEnterpriseAdminTable(network);
         }
    }
    
    private void populateEnterpriseAdminTable(Network network){
        DefaultTableModel model = (DefaultTableModel) enterpriseAdminTable.getModel();
        model.setRowCount(0);
        
        if((network!=null)&&(network.getEnterpriseDirectory()!=null)){
        for(Enterprise enterprise:network.getEnterpriseDirectory().getEnterpriseList()){
            for(UserAccount userAccount:enterprise.getUserAccountDirectory().getUserAccountList()){
                if(!(userAccount.getRole() instanceof PatientRole)){
                    Object[] row = new Object[5];
                    row[0] = enterprise.getName();
                    row[1] = userAccount;
                    row[2] = userAccount.getPerson().getPersonId();
                    row[3] = userAccount.getUserName();
                    row[4] = userAccount.getPassword();
                    model.addRow(row);
            }
          }     
        }
      
     }  
    }
