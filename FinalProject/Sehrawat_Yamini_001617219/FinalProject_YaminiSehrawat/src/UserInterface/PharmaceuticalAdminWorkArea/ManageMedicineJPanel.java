    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
         int selectedRow = medTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row to delete a medicine from the record!");
            return;
        }
        Drug drug = (Drug) medTable.getValueAt(selectedRow, 2);
        int flag= JOptionPane.showConfirmDialog(this, "Are you sure want to remove?","Warning", JOptionPane.YES_NO_OPTION);
        
        if(flag==0){
            enterprise.getDrugCatalog().getDrugCatalog().remove(drug);
            for(WorkRequest  workRequest:enterprise.getWorkQueue().getWorkRequestList()){
               if(workRequest instanceof DrugApprovalRequest){
                   DrugApprovalRequest request = (DrugApprovalRequest) workRequest;
                   request.getDrugCatalog().getDrugCatalog().remove(drug);
                   JOptionPane.showMessageDialog(null, "Record Deleted!");
                }
            }
        }else{
            JOptionPane.showMessageDialog(null, "Record not Deleted!");
        }
        populateMedicineDetailTable();
        
    }//GEN-LAST:event_deleteBtnActionPerformed

