    private void populateMedicineDetailTable(){
        DefaultTableModel model = (DefaultTableModel) medTable.getModel();
            model.setRowCount(0);
            
            for(Drug drug:enterprise.getDrugCatalog().getDrugCatalog()){
                Object[] row = new Object[6];
                row[0] = drug.getDrugId();
                row[1] = drug.getDrugName();
                row[2] = drug;
                row[3] = drug.getDisease().getDiseaseName();
                row[4] = drug.getUses();
                row[5] = drug.getSideEffects();
                model.addRow(row);
                
            }
    }
