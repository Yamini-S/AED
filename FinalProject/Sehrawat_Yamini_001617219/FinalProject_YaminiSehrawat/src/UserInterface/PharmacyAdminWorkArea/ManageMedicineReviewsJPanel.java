    private void populateMedicinesReviewTable(){
        DefaultTableModel model = (DefaultTableModel) medicineResponseTable.getModel();
        model.setRowCount(0); 
        
        for(WorkRequest request:enterprise.getWorkQueue().getWorkRequestList()){
            if(request instanceof DrugReviewRequest){
                DrugReviewRequest drugReviewRequest = (DrugReviewRequest)request;
               for(Drug drug: drugReviewRequest.getDrugCatalog().getDrugCatalog()){
                    Object[] row = new Object[10];
                    row[0] = drugReviewRequest.getRequestID();
                    row[1] = drug.getDrugName();
                    row[2] = drug;
                    row[3] = drug.getDisease().getDiseaseName();
                    row[4] = drug.getSideEffects();
                    row[5] = drugReviewRequest.getSender().getPerson().getName();
                    row[6] = drug.getPharmaceuticalName();
                    row[7] = drugReviewRequest.getCallBackRequest() == null ? ("No") : (drugReviewRequest.getCallBackRequest());
                    row[8] = drugReviewRequest.getStatus();
                    row[9] = drugReviewRequest;
                    model.addRow(row);
                }
            }
        }
    }
