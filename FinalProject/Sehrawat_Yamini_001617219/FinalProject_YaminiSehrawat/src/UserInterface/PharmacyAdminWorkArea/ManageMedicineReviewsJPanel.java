    private void sendBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendBtnActionPerformed
        String issue = issueTextArea.getText();

        if((issue.equals(""))||(issue.equals(null))){
            JOptionPane.showMessageDialog(null, "Please fill up the details in the box provided!");
            return;
        }

        int selectedRow = medicineResponseTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row to proceed");
            return;
        }
        DrugReviewRequest drr = (DrugReviewRequest) medicineResponseTable.getValueAt(selectedRow, 9);
       // DrugReviewRequest request = new DrugReviewRequest();
        drr.setDrugReviews(issue);
        drr.setStatus("Issue sent");
        drr.setReceiver(userAccount);
        drr.setIsReplied("Yes");
        populateMedicinesReviewTable();
        JOptionPane.showMessageDialog(null, "Request Submitted!");
        resetFields();
        
        
    }//GEN-LAST:event_sendBtnActionPerformed

